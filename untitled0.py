import pandas as pd

df = pd.read_csv("messy_customer_data.csv")
df.head()

df.info()

df.describe()

df.isnull().sum()

#Data Cleaning
df['TotalCharges'] = df['TotalCharges'].fillna(df['TotalCharges'].median())

df['Gender'] = df['Gender'].str.strip().str.lower().map({
    'male': 'Male',
    'm': 'Male',
    'female': 'Female',
    'f': 'Female'
})

df['Churn'] = df['Churn'].str.strip().str.lower().map({
    'yes': 'Yes',
    'y': 'Yes',
    'no': 'No',
    'n': 'No'
})

df.drop_duplicates(inplace=True)

# Tenure in years
df['Tenure_Years'] = df['Tenure'] / 12

# Churn flag
df['Churn_Flag'] = df['Churn'].map({'Yes': 1, 'No': 0})

# Risk category
df['Risk'] = df.apply(lambda x: 'High Risk' if x['Tenure'] < 12 and x['MonthlyCharges'] > 70
                     else ('Medium Risk' if x['Tenure'] < 24 else 'Low Risk'), axis=1)

##Basic Analysis
# Churn %
df['Churn'].value_counts(normalize=True) * 100

# Avg charges by churn
df.groupby('Churn')['MonthlyCharges'].mean()

# Tenure vs churn
df.groupby('Churn')['Tenure'].mean()

df.to_csv("final_cleaned_data.csv", index=False)

